package fr.sabb.data.dto;

public class FfbbTeamDto {

    private String name;

    private String ffbbUniqueId;

    private String poule;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFfbbUniqueId() {
        return ffbbUniqueId;
    }

    public void setFfbbUniqueId(String ffbbUniqueId) {
        this.ffbbUniqueId = ffbbUniqueId;
    }

    public String getPoule() {
        return poule;
    }

    public void setPoule(String poule) {
        this.poule = poule;
    }
}
